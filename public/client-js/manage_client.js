function addImageUrl() {
    var url = $('#image_url').val();
    if (url.trim().length == 0) {
        alert('請輸入圖片網址！'); // FIXME: Change to jQuery UI alert box generated by server with i18n
        return;
    }
    $.ajax({
        method: "POST",
        url: "/add_image_url",
        data: {
            url: url
        }
    })
        .done(function (html) {
            $('#images').html(html);
        });
}

